def get_nesa_teaching_content(topic: str) -> dict:
    """Get official NESA teaching content for a given topic"""
    # Map common query terms to focus areas
    topic_lower = topic.lower()
    
    if any(word in topic_lower for word in ['universe', 'space', 'solar', 'planet', 'astronomy', 'observation']):
        return NESA_STAGE4_CONTENT["Observing the Universe"]
    elif any(word in topic_lower for word in ['force', 'motion', 'friction', 'gravity', 'magnet']):
        return NESA_STAGE4_CONTENT["Forces"]
    elif any(word in topic_lower for word in ['cell', 'classification', 'organism', 'microscope']):
        return NESA_STAGE4_CONTENT["Cells and Classification"]
    elif any(word in topic_lower for word in ['solution', 'mixture', 'separation', 'solubility']):
        return NESA_STAGE4_CONTENT["Solutions and Mixtures"]
    elif any(word in topic_lower for word in ['living', 'ecosystem', 'photosynthesis', 'food chain']):
        return NESA_STAGE4_CONTENT["Living Systems"]
    elif any(word in topic_lower for word in ['atom', 'element', 'periodic', 'compound']):
        return NESA_STAGE4_CONTENT["Periodic Table and Atomic Structure"]
    elif any(word in topic_lower for word in ['change', 'chemical change', 'physical change', 'reaction']):
        return NESA_STAGE4_CONTENT["Change"]
    elif any(word in topic_lower for word in ['data', 'graph', 'variable', 'pattern']):
        return NESA_STAGE4_CONTENT["Data Science 1"]
    
    # Default to first topic
    return NESA_STAGE4_CONTENT["Observing the Universe"]


