def get_topic_content(topic):
    """Get syllabus content for a given topic - Stage 4 (Years 7-8)"""
    topic_lower = topic.lower()
    
    # Map common topics to Stage 4 focus areas (check more specific keywords first)
    if any(word in topic_lower for word in ['change', 'physical change', 'chemical change', 'reaction', 'burn', 'rust']):
        return STAGE_4_CONTENT["Change"]
    elif any(word in topic_lower for word in ['data', 'graph', 'pattern', 'variable', 'measurement', 'data science']):
        return STAGE_4_CONTENT["Data Science 1"]
    elif any(word in topic_lower for word in ['cell', 'classification', 'classify', 'organism', 'tissue', 'organ']):
        return STAGE_4_CONTENT["Cells and Classification"]
    elif any(word in topic_lower for word in ['living', 'ecosystem', 'photosynthesis', 'respiration', 'body', 'system']):
        return STAGE_4_CONTENT["Living Systems"]
    elif any(word in topic_lower for word in ['solution', 'mixture', 'separation', 'solvent', 'solute', 'filter']):
        return STAGE_4_CONTENT["Solutions and Mixtures"]
    elif any(word in topic_lower for word in ['atom', 'element', 'periodic', 'compound', 'chemical formula']):
        return STAGE_4_CONTENT["Periodic Table and Atomic Structure"]
    elif any(word in topic_lower for word in ['force', 'motion', 'friction', 'gravity', 'balanced', 'newton']):
        return STAGE_4_CONTENT["Forces"]
    elif any(word in topic_lower for word in ['universe', 'space', 'solar', 'planet', 'star', 'moon', 'galaxy', 'astronomy']):
        return STAGE_4_CONTENT["Observing the Universe"]
    
    # Default to Forces if no match
    return STAGE_4_CONTENT["Forces"]

def get_all_strands():
    """Get all Stage 4 focus areas"""
    return STAGE_4_CONTENT
